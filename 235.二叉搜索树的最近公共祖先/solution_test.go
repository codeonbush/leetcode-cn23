func lowestCommonAncestor(root, p, q *TreeNode) *TreeNode {
	val1 := int(math.Min(float64(p.Val), float64(q.Val)))
	val2 := int(math.Max(float64(p.Val), float64(q.Val)))
	return find(root, val1, val2)

}

