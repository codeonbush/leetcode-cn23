/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */
func generateTrees(n int) []*TreeNode {
	if n == 0 {
		return []*TreeNode{}
	}
	var build func(l, r int) []*TreeNode
	build = func(l, r int) []*TreeNode {
		res := make([]*TreeNode, 0)
		if l > r {
			res = append(res, nil)
			return res
		}
		for i := l; i <= r; i++ {
			//ååºéå†
			leftTree := build(l, i-1)
			rightTree := build(i+1, r)
			//åœ¨rootä¸ŠæŒ‚ä¸Šæ‰€æœ‰å¯èƒ½çš„å·¦å³å­æ ‘
			for _, left := range leftTree {
				for _, right := range rightTree {
					root := &TreeNode{Val: i}
					root.Left = left
					root.Right = right
					res = append(res, root)
				}
			}
		}
		return res
	}
	return build(1, n)
}

