/**
 * Definition for singly-linked list.
 * type ListNode struct {
 *     Val int
 *     Next *ListNode
 * }
 */
func partition(head *ListNode, x int) *ListNode {
	d1 := &ListNode{-1, nil}
	d2 := &ListNode{-1, nil}

	p1, p2 := d1, d2
	p := head

	for p != nil {
		if p.Val >= x {
			p2.Next = p
			p2 = p2.Next
		} else {
			p1.Next = p
			p1 = p1.Next
		}
		temp := p.Next
		p.Next = nil
		p = temp
	}
	p1.Next = d2.Next
	return d1.Next
}

